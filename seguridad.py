    def confirmarOpcion(self, opcion):
        #confirmar opci√≥n que se eligi√≥ y llamar a funci√≥n para guardar los datos 
        alerta = QMessageBox() 
        alerta.setIcon(QMessageBox.Information) 
        alerta.setText("La opci√≥n que elegi√≥ es: "+opcion) 
        alerta.setInformativeText("Si la opci√≥n es correcta presione OK para continuar") 
        alerta.setWindowTitle("IMPORTANTE") 
        alerta.setStandardButtons(QMessageBox.Ok | QMessageBox.Cancel) 
        alerta.button(QMessageBox.Cancel).setVisible(False)
        valorPresionado = alerta.exec_()
        if valorPresionado == QMessageBox.Ok:
            if opcion == 'Ataques bloqueados':
                guardarDatosVar(3, 'seguridad', self.archivo, 'id_ataque', 'seg_ataque')
            elif opcion == 'Cambios':
                guardarDatosVar(3, 'seguridad', self.archivo, 'id_cambios', 'seg_cambios')
            elif opcion == 'Incidentes':
                guardarDatosVar(3, 'seguridad', self.archivo, 'id_incidente', 'seg_incidente')
            else:
                guardarDatosVar(3, 'seguridad', self.archivo, 'id_usuario', 'seg_us')

